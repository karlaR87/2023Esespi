    private void ShowHide(int a)
    {
        if (a == 1) {jLabel1.setVisible(false); x=0; }else{
        if(x == 200)
        {
           Hide();
        }
        else
        {
        jLabel1.setVisible(true); slideMenu.setSize(200, 600);
            if(x == 0)
            {
                slideMenu.show();
                slideMenu.setSize(200, 600);
                Thread th = new Thread () {
                    @Override
                    public void run()
                    {

                        try
                        {
                            for (int i = 0; i<= x; i++)
                            {
                                Thread.sleep(1);
                                slideMenu.setSize(i, 600);
                            }
                        }
                        catch(Exception e)
                        {
                            System.out.println("Algo paso en el thread2");
                        }                     } 
                };th.start();
                x = 200;
            }
        }   
    }
 }
    
    public void Hide()
    {
         slideMenu.setSize(200, 600); jLabel1.setVisible(false); 
            Thread th = new Thread() {
                @Override
                public void run()
                {
                    try
                    {
                        for(int i = 200; i >= 0; i--)
                        {
                            Thread.sleep(1);
                            slideMenu.setSize(i, 600);
                        }
                    } 
                    catch(Exception e)
                    {
                        System.out.println("Algo paso en el thread");
                    }
                }
            }; th.start();
            x = 0;
    }
